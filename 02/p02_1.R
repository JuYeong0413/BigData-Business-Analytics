## 데이터프레임 dataframe 
#  두객체에 해당하는 데이터 프레임 만들기
x <- data.frame(성명=c("홍길동", "손오공"), 나이=c(20, 30), 주소=c("서울", "부산"))
x

# 행/열 추가와 요소값 확인
# 데이터 프레임 x에 열(학과) 추가
x <- cbind(x, 학과=c("전산학", "경영학")) ## column bind
x
# 데이터 프레임 x에 행(장발장) 추가
x <- rbind(x, data.frame(성명="장발장", 나이=40, 주소="파리", 학과="전산학")) ## row bind
x
# 3행2열의 요소값
x[3, 2]
# 3행의 모든값
x[3, ]
# 2행을 제외한 모든값
x[-2, ]
# "성명" 요소 = x[1]과 동일한 결과
x["성명"]
# "성명" 요소값
x$성명
x[["성명"]]
x[[1]]
# 1열 요소 값에서 두 번째 값
x[[1]][2]
