# 벡터 만들기 
# "<-"는 "="와 같은 의미이지만, 객체 할당 구분을 위하여 "<-"를 주로 씀
# "<-"를 쓸 때는 객체 입력을 알아보기 쉽게 양 옆에 한칸씩 띄어쓰기 함
x <- c(80, 85, 70)
x


# 벡터의 원소
# x 벡터 만들기
x <- c(1, 2, 3, 4, 5)
# 두번째 원소
x[2]
# 1,3,5 번째 원소
x[c(1, 3, 5)]
# 2,4번째 제외한 원소
x[-c(2, 4)]
# 원소의 값이 2보다 큰 값들만 출력
x[x>2]
# 원소의 값이 2 이상이고 4 이하인 값들만 출력
x[x>=2 & x<=4]
# 2번째 원소의 값을 20번째로 수정
x[2] <- 20
# 3,4번째 원소들의 값을 모두 15로 수정
x[c(3, 4)] <- 15
# 15 이하인 원소 값들을 모두 10으로 수정
x[x<=15] <- 10


x<-seq(1:10)
# x 벡터의 원소값들의 평균
mean(x)
# x 벡터의 원소값들의 분산
var(x)
# x 벡터의 원소값들의 표준편차
sd(x)
# x 벡터의 원소값들의 제곱근
sqrt(x)
# x 벡터의 원소값들의 개수
length(x)
# x 벡터의 원소값들의 절대값
abs(x)


